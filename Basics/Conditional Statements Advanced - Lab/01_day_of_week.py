# 1. day of week

entry = int(input())

if entry == 1 :
    print('Monday')
elif entry == 2 :
    print('Tuesday')
elif entry == 3 :
    print('Wednesday')
elif entry == 4 :
    print('Thursday')
elif entry == 5 :
    print('Friday')
elif entry == 6 :
    print('Saturday')
elif entry == 7 :
    print('Sunday')
else :
    print('Error')